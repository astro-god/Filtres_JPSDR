static inline void Move_Full(const void *src_, void *dst_, const int32_t w,const int32_t h,
		const ptrdiff_t src_pitch,const ptrdiff_t dst_pitch)
{
	const uint8_t *src=(uint8_t *)src_;
	uint8_t *dst=(uint8_t *)dst_;

	if ((h==1) || ((src_pitch==dst_pitch) && (abs(src_pitch)==w)))
	{
		if (src_pitch<0)
		{
			src+=(h-1)*src_pitch;
			dst+=(h-1)*dst_pitch;
		}
		memcpy(dst,src,(size_t)h*(size_t)w);
	}
	else
	{
		for(int32_t i=0; i<h; i++)
		{
			memcpy(dst,src,w);
			src+=src_pitch;
			dst+=dst_pitch;
		}
	}
}


void JPSDR_InterlaceMotion::Run()
{
	Image_Data idata;
	int32_t k,w_test;
	const void *src;
	const uint32_t *src_32,*s_32;
	bool same_picture;
	void *s,*dst;

	idata=image_data;

	if ((idata.src_h0==0) || (idata.src_w0==0)) return;

	if (g_VFVAPIVersion>=12)
	{
		const VDXPixmap& pxdst=*fa->dst.mpPixmap;
		const VDXPixmap& pxsrc=*fa->src.mpPixmap;

		idata.src_plane0=pxsrc.data;
		idata.src_plane1=pxsrc.data2;
		idata.src_plane2=pxsrc.data3;
		idata.dst_plane0=pxdst.data;
		idata.dst_plane1=pxdst.data2;
		idata.dst_plane2=pxdst.data3;
	}
	else
	{
		idata.src_plane0=fa->src.data;
		idata.dst_plane0=fa->dst.data;
		idata.src_plane1=NULL;
		idata.src_plane2=NULL;
		idata.dst_plane1=NULL;
		idata.dst_plane2=NULL;
	}

	const int32_t w=idata.src_w0,h=idata.src_h0;
	const ptrdiff_t src_pitch=idata.src_pitch0,dst_pitch=idata.dst_pitch0,buffer_pitch=idata.src_pitch0_al;
	const uint32_t line0=idata.src_line0;

	src = idata.src_plane0;
    dst = idata.dst_plane0;
	s= mem;

	if (nbre_images==0)
	{
		switch(idata.video_mode)
		{
			case 0 :
			case 1 :
			case 2 :
			case 3 :
				Move_Full(src,s,line0,h,src_pitch,buffer_pitch);
				Move_Full(s,dst,line0,h,buffer_pitch,dst_pitch);
				break;
		}
		nbre_images++;
		return;
	}
	
	switch(idata.video_mode)
	{
		case 0 :
		case 1 :
			w_test=w;
			break;
		case 2 :
		case 3 :
			w_test=(w+1)>>1;
			break;
	}

	src_32=(uint32_t *)src;
	s_32=(uint32_t *)s;

	k=0;
	do
	{
		int32_t j=0;
		do
		{
			same_picture=(src_32[j]==s_32[j]);
			j+=4;
		}
		while (same_picture && (j<w_test));
		k+=4;
        s_32 = (uint32_t *)((uint8_t *)s_32 + (buffer_pitch << 2));
        src_32 = (uint32_t *)((uint8_t *)src_32 + (src_pitch << 2));
	}
	while (same_picture && (k<h));

	if (!same_picture)
	{
		switch(idata.video_mode)
		{
			case 0 :
			case 1 :
			case 2 :
			case 3 :
				Move_Full(src,dst,line0,h,src_pitch,dst_pitch);
				break;
		}
		nbre_images++;
		return;
	}

	for (int32_t i=0; i<h; i+=2)
	{
		memcpy(dst,s,line0);
		s = (void *)((uint8_t *)s + (buffer_pitch << 1));
		dst = (void *)((uint8_t *)dst + (dst_pitch << 1));
	}
	dst=(void *)((uint8_t *)idata.dst_plane0 + dst_pitch);
	src=(void *)((uint8_t *)src + src_pitch);
	for (int32_t i=1; i<h; i+=2)
	{
		memcpy(dst,src,line0);
		src = (void *)((uint8_t *)src + (src_pitch << 1));
		dst = (void *)((uint8_t *)dst + (dst_pitch << 1));
	}

	src = idata.src_plane0;
	s= mem;
	switch(idata.video_mode)
	{
		case 0 :
		case 1 :
		case 2 :
		case 3 :
			Move_Full(src,s,line0,h,src_pitch,buffer_pitch);
			break;
	}
	nbre_images++;
}



